        y = 1
        for i in self.level:
            if i == 0:
                pygame.draw.rect(screen, pygame.Color("Gray"), pygame.Rect(x, y, 10, 10))
            x += 11
            if x == 441:
                y += 11
                x = 1
        pygame.image.save(screen, "images/gui.png")

